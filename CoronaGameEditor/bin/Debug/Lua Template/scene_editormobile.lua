							display.screenOriginY + display.contentHeight /2,50,50)
							
	textBox.text = "Enter the tiles map name..."
	
	local onEvent = function(event)
		local phase = event.phase
		
		if(phase == "began") then 
			textBox.text = ""
			
		elseif(phase == "ended") then 
			editorEngine:loadTilesMap(textBox.text)
			textBox:removeSelf()
		end
	end
	
	textBox:addEventListener("userInput",onEvent)
	
end

